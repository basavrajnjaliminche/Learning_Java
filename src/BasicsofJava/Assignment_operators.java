package BasicsofJava;

public class Assignment_operators {

    public static void main(String[] args) {

        int a=12;
        int b=7;

       /*
            a+=b --> a=a+b
            a-=b --. a=a-b
            a*=b --. a=a*b
            a/=b --. a=a/b
            a%=b --. a=a%b
        */

        a+=b;
        System.out.println(a);

        /*  Relational Operator
            1. ==   --> is equal to
            2. !=   --> not equal to
            3. >    -->grater than
            4. <    -->smaller than
            5. >=   --> grater than equal to
            6. <=   --> smaller than equal to

           Logical Operators
           1. && (Logical AND) -->   both are true
           2. || (Logical OR)  --> only one is true
           3. !  (Not operator)

           Increment operators
           1.   a++ = a+1
           2.   a-- = a-1

           Ternary Operators
        */

    }
}
